package antgame;

/**
 *
 * @author Software Engineering Group 12
 * Class to represent position, an X & Y coordinate.
 */
public class Position {
    int x;
    int y;
    
    /**
     * Constructor for a position
     * @param x coordinate
     * @param y coordinate
     */
    Position(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
